package com.startjava.lesson_1.game;

public class Variable {
    public static void main(String[] args) {     
        byte monitors = 1;
        System.out.println(monitors);

        short speakers = 2;
        System.out.println(speakers);

        int cpuCore = 8;
        System.out.println(cpuCore);

        long cpuThread = 16L;
        System.out.println(cpuThread);

        float cpuSpeed = 3.6f;
        System.out.println(cpuSpeed);

        double monitorSize = 27.0d;
        System.out.println(monitorSize);

        char cpuIndex = 'K';
        System.out.println(cpuIndex);

        boolean laptop = false;
        System.out.println(laptop);
    }
}